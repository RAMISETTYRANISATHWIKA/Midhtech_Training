name1="Ram"
name2="Sita"

#Operations

#concatenation
print("concatenation:", name1+" "+ name2)

#Repetition
print("Repitition:", (name1+" ")*3)

#Indexing
print("Indexing:", name1[0])

#Slicing
print("Slicing:", name1[0:10])

#len()
print("Length:", len(name1))

#Membership(in, not in)
print("in:", "R" in name1)
print("not in:", "R" not in name1)

#Methods

#upper
print("upper:", name1.upper())

# lower()
print("lower:", name1.lower())

#replace()
print("Replace:", name1.replace("Ram", "Raam"))

#original string dont change
print("Original string:", name1)

